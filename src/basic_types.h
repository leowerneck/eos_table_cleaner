#ifndef BASIC_TYPES_H
#define BASIC_TYPES_H

#include <stdint.h>

// Signed integers
typedef int8_t  i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;
typedef i64     isize;

// Unsigned integers
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;
typedef u64      usize;

// Floating points
typedef float  f32;
typedef double f64;

#endif // BASIC_TYPES_H
